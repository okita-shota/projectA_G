// マイページの表示/非表示を切り替え
const openMypageButton = document.getElementById('openMypage');
const mypageContainer = document.getElementById('mypageContainer');

openMypageButton.addEventListener('click', () => {
  if (mypageContainer.style.display === 'none') {
    mypageContainer.style.display = 'block';
    openMypageButton.textContent = 'マイページを閉じる';
  } else {
    mypageContainer.style.display = 'none';
    openMypageButton.textContent = 'マイページ';
  }
});

